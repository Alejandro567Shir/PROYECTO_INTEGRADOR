function seleccionarRol(rol) {
  if (rol === "estudiante") {
    alert("Entrando como Estudiante");
  } else if (rol === "docente_f1") {
    alert("Entrando como Docente F1");
  } else if (rol === "director_f2") {
    alert("Entrando como Director F2");
  } else if (rol === "secretaria") {
    alert("Entrando como Secretaría");
  } else if (rol === "coordinador") {
    alert("Entrando como Coordinador");
  }
}